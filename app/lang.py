class Lang:
    """Language manager"""

    def __init__(self, lang="en"):
        self.lang = lang
        self.texts = TRANSLATIONS.get(lang, TRANSLATIONS["en"])

    def get(self, key: str, **kwargs) -> str:
        """Get translated text"""
        text = self.texts.get(key, key)
        if kwargs:
            return text.format(**kwargs)
        return text

    def set_lang(self, lang: str):
        """Change language"""
        self.lang = lang
        self.texts = TRANSLATIONS.get(lang, TRANSLATIONS["en"])
